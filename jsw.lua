function TIC()

	if btn(2) then xscroll=xscroll-1 end
	if btn(3) then xscroll=xscroll+1 end

	if btnp(4) then roomnumber = roomnumber - 1 end
	if btnp(5) then roomnumber = roomnumber + 1 end

	if roomnumber < 0 then roomnumber = 0 end

	draw()
end

-- set spritesheet pixel
function sset(x,y,c)
    local addr=0x4000+(x//8+y//8*16)*32 -- get sprite address
    poke4(addr*2+x%8+y%8*8,c) -- set sprite pixel
end

-- get spritesheet pixel
function sget(x,y)
    local addr=0x4000+(x//8+y//8*16)*32 -- get sprite address
    return peek4(addr*2+x%8+y%8*8) -- get sprite pixel
end

function draw()
	cls(0)
	local offset = (256*roomnumber)+1

	-- 128b layout

	tilemap = {}
	
	for i=0,511 do
	 local pair = i % 4
		local tbyte = rooms[offset+math.floor(i/4)]
		local tile = 0

		if pair == 0 then
			tile = (tbyte & 0xC0)>>6 
		end
		if pair == 1 then
			tile = (tbyte & 0x30)>>4 
		end
		if pair == 2 then
			tile = (tbyte & 0x0C)>>2 
		end
		if pair == 3 then
			tile = tbyte & 0x03
		end

		table.insert(tilemap,tile)
	end
	
	offset=offset+128
	
 -- 32b room name
 
 local text = ""
 for i=0,31 do
 	local chara = string.char(rooms[offset+i])
  if (chara == " ") then chara = " " end
 	text = text .. chara
 end
 
 text = text .. roomnumber
 
 print(text,0,128,15,true)

 offset = offset+32
 
	-- 54b attrib + tile gfx
 
 i = 0
 for ti=0,5 do
		local cbyte = rooms[offset+i]

		local paper = ((cbyte)>>3)
		local ink = (cbyte & 0x0f)

		i = i + 1
 	for y=0,7 do
			local pbyte = rooms[offset+i]
			
	 	for x=0,7 do
				local pixel = pbyte & (1<<(7-x))

				if pixel == 0 then sset(x+(ti*8),y,paper) end
				if pixel ~= 0 then sset(x+(ti*8),y,ink) end
 	 end
  	i = i + 1
  end
  
 end
 
 -- tile gfx defined, draw room
	for i=0,511 do
		local tile = tilemap[i+1]

		local x = (i%32)
		local y = math.floor(i/32)

		if tile == 0 then
		spr(0,xscroll+x*8,yscroll+y*8)
		end

		if tile == 1 then
		spr(1,xscroll+x*8,yscroll+y*8)
		end

		if tile == 2 then
		spr(2,xscroll+x*8,yscroll+y*8)
		end

		if tile == 3 then
		spr(3,xscroll+x*8,yscroll+y*8)
		end
	end

 offset = offset+54

 -- 4b conveyor

	local conv_dir = rooms[offset]
	local conv_pos = 0x0000
	local conv_pos1 = rooms[offset+2]
	local conv_pos2 = rooms[offset+1]

	conv_pos = (conv_pos1<<8)
	conv_pos = conv_pos + conv_pos2

 -- offset to start from 0x0000
	conv_pos = conv_pos - 0x5e00

	local conv_len = rooms[offset+3]
	local xx = (conv_pos % 32)*8
	local yy = math.floor(conv_pos/32)*8

	for j = 0, conv_len-1 do
		spr(5,xx+j*8+xscroll,yy)
 end

 offset = offset+4

 -- 4b ramp

	local ramp_dir = rooms[offset]
	local ramp_pos = 0x0
	local ramp_pos1 = rooms[offset+2]
	local ramp_pos2 = rooms[offset+1]

	ramp_pos = (ramp_pos1<<8)
	ramp_pos = ramp_pos + ramp_pos2

 ramp_pos = ramp_pos - 0x5e00
	xx = (ramp_pos % 32)*8
	yy = math.floor(ramp_pos/32)*8

	local ramp_len = rooms[offset+3]

	if (ramp_dir == 0) then

		local y = 0
		
		for x = 0, ramp_len-1, 1 do

			spr(4,-x*8+xx+xscroll,yy-y*8)
			y = y + 1
		end
	end


	if (ramp_dir == 1) then

		local y = 0

		for x = 0, ramp_len-1 do

			spr(4,xx+xscroll+x*8,yy+y*8)
			y = y - 1
		end
	end

 offset = offset+4

 -- 1b border
 
 local border = rooms[offset]

 offset = offset+1

 -- 2b unused

 offset = offset+2

 -- 8b item gfx (spr 6)

 i = 0
	ti = 6
	for y=0,7 do
		local pbyte = rooms[offset+i]
		
 	for x=0,7 do
			local pixel = pbyte & (1<<(7-x))

			if pixel == 0 then sset(x+(ti*8),y,0) end
			if pixel ~= 0 then sset(x+(ti*8),y,15) end
	 end
 	i = i + 1
 end
 
 offset = offset+8

 -- 4b exits

 offset = offset+4

 -- 3b unused

 offset = offset+3

 -- 8x2b entities	

 offset = offset+16

end

